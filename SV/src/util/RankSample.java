package util;

public class RankSample {

	private int ARank;
	private int CRank;
	private int GRank;
	private int TRank;
	
	
	
	public RankSample(int aRank, int cRank, int gRank, int tRank) {
		super();
		ARank = aRank;
		CRank = cRank;
		GRank = gRank;
		TRank = tRank;
	}
	public int getARank() {
		return ARank;
	}
	public void setARank(int aRank) {
		ARank = aRank;
	}
	public int getCRank() {
		return CRank;
	}
	public void setCRank(int cRank) {
		CRank = cRank;
	}
	public int getGRank() {
		return GRank;
	}
	public void setGRank(int gRank) {
		GRank = gRank;
	}
	public int getTRank() {
		return TRank;
	}
	public void setTRank(int tRank) {
		TRank = tRank;
	}
	
	
	
}
